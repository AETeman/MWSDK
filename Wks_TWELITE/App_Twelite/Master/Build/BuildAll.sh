#!/bin/sh

BUILDMODEL="BLUE"
#BUILDMODEL="RED"

if [ ${BUILDMODEL} = "BLUE" ]; then
	make TWE_CHIP_MODEL=JN5164 APP_UART_CONFIG=CONFIG_NORMAL clean
	make TWE_CHIP_MODEL=JN5164 APP_UART_CONFIG=CONFIG_NORMAL  all -j 4
	make TWE_CHIP_MODEL=JN5164 APP_UART_CONFIG=CONFIG_NORMAL TWE_DEVKIT=MONOSTICK clean
	make TWE_CHIP_MODEL=JN5164 APP_UART_CONFIG=CONFIG_NORMAL TWE_DEVKIT=MONOSTICK all -j 4
elif [ ${BUILDMODEL} = "RED" ]; then
	make TWELITE=RED APP_UART_CONFIG=CONFIG_NORMAL clean
	make TWELITE=RED APP_UART_CONFIG=CONFIG_NORMAL all
	make TWELITE=RED APP_UART_CONFIG=CONFIG_NORMAL TWE_DEVKIT=MONOSTICK clean
	make TWELITE=RED APP_UART_CONFIG=CONFIG_NORMAL TWE_DEVKIT=MONOSTICK all
else
	echo "Error"
fi
